    def find_path(self, level, target):
        open_list = []
        closed_list = set()
        start_node = (self.center_x // 30, self.center_y // 32)
        target_node = (target[0] // 30, target[1] // 32)

        heapq.heappush(open_list, (0, start_node))

        while open_list:
            current_cost, current_node = heapq.heappop(open_list)

            if current_node == target_node:
                return current_cost

            if current_node in closed_list:
                continue

            closed_list.add(current_node)

            x, y = current_node
            neighbors = [(x-1, y), (x+1, y), (x, y-1), (x, y+1)]

            for neighbor in neighbors:
                nx, ny = neighbor
                if 0 <= nx < len(level[0]) and 0 <= ny < len(level):
                    if level[ny][nx] < 3 or level[ny][nx] == 9:
                        neighbor_cost = current_cost + 1
                        heapq.heappush(open_list, (neighbor_cost + heuristic(neighbor, target_node), neighbor))

        return float('inf')

